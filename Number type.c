//Program name: Number type

#include<stdio.h>
int main()
{
    int num1=10;
    float num2=20.5;
    double num3=50.5;
    char ch= 'a';

    printf("Integer type number: %d\n",num1);
    printf("Floating type number: %.2f\n",num2);
    printf("Double type number: %lf\n",num3);
    printf("Character type: %c\n",ch);

    return 0;
}
